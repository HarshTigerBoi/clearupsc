const angles = [
  "definition and conceptual clarity",
  "constitutional or institutional angle",
  "historical background",
  "current affairs linkage",
  "policy challenge",
  "committee or report relevance",
  "implementation bottleneck",
  "way forward",
];

function key(parts) {
  return parts.join("_").toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");
}

function esc(value) {
  return String(value).replaceAll("'", "''");
}

const lines = [
  "-- Generated by scripts/build-clearupsc-seed.mjs",
  "-- Run supabase/schema.sql first, then this seed file.",
  "",
];

const topicRows = [];
for (const [paper, paperConfig] of Object.entries(papers)) {
  for (const [area, subtopics] of Object.entries(paperConfig.areas)) {
    const areaKey = key([paper, area]);
    topicRows.push({ key: areaKey, subject: paper, parent: null, title: area, stage: paperConfig.stage, weight: 4 });
    for (const subtopic of subtopics) {
      const subKey = key([paper, area, subtopic]);
      topicRows.push({ key: subKey, subject: paper, parent: areaKey, title: subtopic, stage: paperConfig.stage, weight: 3 });
      for (const angle of angles) {
        topicRows.push({ key: key([paper, area, subtopic, angle]), subject: paper, parent: subKey, title: `${subtopic}: ${angle}`, stage: paperConfig.stage, weight: angle.includes("current") || angle.includes("policy") ? 5 : 2 });
      }
    }
  }
}

lines.push("insert into public.topics (key, subject, parent_key, title, exam_stage, upsc_weightage) values");
lines.push(
  topicRows
    .slice(0, 430)
    .map((topic, index, array) => `('${esc(topic.key)}','${esc(topic.subject)}',${topic.parent ? `'${esc(topic.parent)}'` : "null"},'${esc(topic.title)}','${esc(topic.stage)}',${topic.weight})${index === array.length - 1 ? "" : ","}`)
    .join("\n"),
);
lines.push("on conflict (key) do update set title = excluded.title, parent_key = excluded.parent_key, exam_stage = excluded.exam_stage, upsc_weightage = excluded.upsc_weightage;");
lines.push("");

const subjects = ["History", "Polity", "Geography", "Economy", "Environment", "Science"];
const questionRows = [];
for (let index = 0; index < 3000; index += 1) {
  const topic = topicRows[index % Math.min(topicRows.length, 430)];
  const subject = subjects[index % subjects.length];
  questionRows.push({
    id: `seed_mcq_${String(index + 1).padStart(3, "0")}`,
    topicKey: topic.key,
    subject,
    year: 2014 + (index % 12),
    text: `Practice question ${index + 1}: Which statement best captures ${topic.title} for UPSC preparation?`,
    correct: ["A", "B", "C", "D"][index % 4],
    options: [
      `${topic.title} should be understood with definition, context and examples.`,
      `${topic.title} is only useful for interview and can be skipped for Prelims.`,
      `${topic.title} has no connection with current affairs or policy analysis.`,
      `${topic.title} should be memorised without linking it to syllabus demand.`,
    ],
  });
}

lines.push("insert into public.questions (id, topic_key, question_text, question_type, year, source, difficulty, model_answer, tags) values");
lines.push(
  questionRows
    .map((question, index, array) => `('${question.id}','${esc(question.topicKey)}','${esc(question.text)}','mcq',${question.year},'seed_practice',3,'${esc("Correct approach: build conceptual clarity, link to syllabus, add examples and revise through practice.")}',array['${esc(question.subject)}','practice'])${index === array.length - 1 ? "" : ","}`)
    .join("\n"),
);
lines.push("on conflict (id) do update set question_text = excluded.question_text, topic_key = excluded.topic_key, tags = excluded.tags;");
lines.push("");

lines.push("insert into public.question_options (question_id, option_label, option_text, is_correct) values");
lines.push(
  questionRows
    .flatMap((question) => question.options.map((option, optionIndex) => {
      const label = ["A", "B", "C", "D"][optionIndex];
      return `('${question.id}','${label}','${esc(option)}',${label === question.correct})`;
    }))
    .map((row, index, array) => `${row}${index === array.length - 1 ? "" : ","}`)
    .join("\n"),
);
lines.push("on conflict do nothing;");
lines.push("");

